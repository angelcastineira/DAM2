# --Definicion--
# tipo estructurado e inmutable.
# secuencia ordenada de elementos.
# pueden ser de cualquier tipo y no tienen pq ser todos del mismo tipo.
# encerrados entre parentesis y separados por comas. Aunque parentesis se pueden omitir.

# --Ejemplos--
# tupla vacia
vacia = ()
print(type(vacia)) # ><class 'tuple'>

numeros = (1,2,3)
mezcla = ("Hola", 3, True)
fecha = 2, " feb ", 2022

# cd tupa = una sola coma = coma anonima
eleUnico = ("hola",)
elUnicoSinPara = "Hola",
print(type(elUnicoSinPara)) # ><class 'tuple'>
